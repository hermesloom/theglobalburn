#!/usr/bin/env node
/**
 * Generates SQL to insert missing memberships for Stripe webhook failures.
 * Excludes payment intents that already have a membership (run verify-webhook-affected-users.sql first).
 *
 * Usage: node scripts/generate-webhook-remediation-sql.mjs [--exclude pi_xxx,pi_yyy]
 * Output: misc/webhook-remediation-insert.sql
 *
 * Then: npm run supabase:sql -- -f misc/webhook-remediation-insert.sql
 */

import { readFileSync, writeFileSync } from "fs";
import { join } from "path";

const dataPath = join(process.cwd(), "misc/webhook-remediation-data.json");
const outPath = join(process.cwd(), "misc/webhook-remediation-insert.sql");

const args = process.argv.slice(2);
let excludeIds = new Set();
const excludeArg = args.find((a) => a.startsWith("--exclude="));
if (excludeArg) {
  excludeArg
    .split("=")[1]
    .split(",")
    .forEach((id) => excludeIds.add(id.trim()));
}
// Default: exclude pi_3T9SiOEuBjGnolU20yNvCQGx (Kalle - has membership per Phase 1)
excludeIds.add("pi_3T9SiOEuBjGnolU20yNvCQGx");

const data = JSON.parse(readFileSync(dataPath, "utf8"));
const events = data.failed_events.filter((e) => !excludeIds.has(e.payment_intent));

const inserts = events.map((e) => {
  const pi = e.payment_intent;
  const price = e.price;
  const currency = e.price_currency;
  const mprId = e.membership_purchase_right_id;
  return `-- ${e.customer_email}: ${e.customer_name}
INSERT INTO burn_memberships (project_id, owner_id, first_name, last_name, birthdate, stripe_payment_intent_id, price, price_currency, metadata, is_low_income, is_non_transferable)
SELECT
  p.project_id, p.owner_id,
  p.first_name, p.last_name, p.birthdate,
  '${pi}', ${price}, '${currency}', p.metadata,
  (${price} = (SELECT bc.membership_price_tier_1 FROM burn_config bc WHERE bc.project_id = p.project_id LIMIT 1)),
  COALESCE(p.is_non_transferable, false)
FROM burn_membership_purchase_rights p
WHERE p.id = '${mprId}'
AND p.first_name IS NOT NULL AND p.last_name IS NOT NULL AND p.birthdate IS NOT NULL
AND NOT EXISTS (SELECT 1 FROM burn_memberships m WHERE m.stripe_payment_intent_id = '${pi}')
ON CONFLICT (project_id, first_name, last_name, birthdate) DO UPDATE SET
  stripe_payment_intent_id = COALESCE(burn_memberships.stripe_payment_intent_id, EXCLUDED.stripe_payment_intent_id),
  price = EXCLUDED.price,
  price_currency = EXCLUDED.price_currency
WHERE burn_memberships.stripe_payment_intent_id IS NULL;`;
});

const sql = `-- Generated by scripts/generate-webhook-remediation-sql.mjs
-- Run: npm run supabase:sql -- -f misc/webhook-remediation-insert.sql
-- Inserts missing memberships for ${events.length} webhook-affected users.

${inserts.join("\n\n")}
`;

writeFileSync(outPath, sql);
console.log(`Wrote ${events.length} INSERT statements to ${outPath}`);
